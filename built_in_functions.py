



def ll_toTextAppend(
    text_input:str,
    text_to_append:str):

    return text_input \
        + text_to_append


def ll_toTextReversed(
    text_input:str):

    return text_input \
        [::-1]


def ll_toTextUppercase(
    text_input:str):

    return text_input \
        .upper()


def ll_print(
    text_input:str):

    print(text_input)

